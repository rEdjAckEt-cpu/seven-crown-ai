col_env1, col_env2 = st.columns(2)
with col_env1:
    weather = st.radio("想定する天候", ["晴", "雨", "雪"], horizontal=True)
with col_env2:
    condition = st.radio("想定する馬場状態", ["良", "稍重", "重", "不良"], horizontal=True)

st.markdown("---")
st.markdown("### 🐎 出走予定馬の入力")
